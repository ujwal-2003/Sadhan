    private void displayQR() {
    paymentMethodController controller = new paymentMethodController();
    // Use the controller to fetch the model containing the bytes
    paymentMethodModel model = controller.getCompanyQR(this.compId);

    if (model != null && model.getCompanyQr() != null && model.getCompanyQr().length > 0) {
        // Convert byte array to ImageIcon
        javax.swing.ImageIcon icon = new javax.swing.ImageIcon(model.getCompanyQr());
        
        // Scale the image to fit your label (210x210)
        java.awt.Image img = icon.getImage().getScaledInstance(210, 210, java.awt.Image.SCALE_SMOOTH);
        
        jLabel17.setIcon(new javax.swing.ImageIcon(img));
        jLabel17.setText(""); // Remove the "Company QR" text
    } else {
        // Case: No image found in database
        jLabel17.setIcon(null);
        jLabel17.setText("QR Not Available");
        jLabel17.setForeground(java.awt.Color.RED);
        jLabel17.setHorizontalAlignment(javax.swing.SwingConstants.CENTER);
    }
}
    private void toggleReceiptComponents() {
        if (jCheckBox1.isSelected()) { // Cash selected
            jButton1.setEnabled(false);       
            jButton1.setBackground(Color.LIGHT_GRAY); 
            jLabel15.setText("Not Required");  
            jLabel16.setText("Cash Payment selected");
            selectedReceiptFile = null;       
        } else if (jCheckBox2.isSelected()) { // Online selected
            jButton1.setEnabled(true); 
            jButton1.setBackground(new Color(51, 255, 0)); // Bright green
            jLabel15.setText("No file selected");
            jLabel16.setText("Please upload receipt image");
        } else { // No selection
            jButton1.setEnabled(false);
            jButton1.setBackground(Color.LIGHT_GRAY);
            jLabel15.setText("Select payment method");
            jLabel16.setText("Select payment method");
            jLabel16.setHorizontalAlignment(javax.swing.SwingConstants.CENTER);
        }
    }

    



    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       JFileChooser chooser = new JFileChooser();
        int result = chooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            selectedReceiptFile = chooser.getSelectedFile();
            jLabel15.setText(selectedReceiptFile.getName()); 
            
            try {
                java.awt.Image img = javax.imageio.ImageIO.read(selectedReceiptFile);
                if (img != null) {
                    java.awt.Image scaledImg = img.getScaledInstance(150, 100, java.awt.Image.SCALE_SMOOTH);
                    jLabel16.setIcon(new javax.swing.ImageIcon(scaledImg));
                    jLabel16.setText(""); 
                }
            } catch (java.io.IOException e) {
                jLabel16.setText("Invalid Image");
            }
        }
    
    
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         String ref = jTextField1.getText().trim();

    // 1. Validate Reference Number
    if (ref.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter Transaction Reference Number.");
        return;
    }

    // 2. Determine Payment Method
    String method = jCheckBox1.isSelected() ? "Cash" : "Online";

    // 3. Mandatory Receipt Check for Online Payments
    if (method.equals("Online") && selectedReceiptFile == null) {
        JOptionPane.showMessageDialog(this, "Please upload a screenshot of your bank receipt for Online payments.");
        return; 
    }

    // 4. Status is forced to pending
    String status = "pending"; 

    // 5. Call Controller
    paymentMethodController controller = new paymentMethodController();
    boolean success = controller.processPayment(bId, cId, compId, amt, method, ref, selectedReceiptFile, status);

    // 6. Show result
    if (success) {
        JOptionPane.showMessageDialog(this, "Payment recorded successfully! Waiting for company approval.");
        this.dispose(); // close payment window
    } else {
        JOptionPane.showMessageDialog(this, "Payment failed. Check console for errors.");
    }
    }//GEN-LAST:event_jButton2ActionPerformed


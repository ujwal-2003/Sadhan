    private void ChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangeActionPerformed
    String currentPass = new String(user_type_Current_Password.getPassword());
    String newPass = new String(user_type_New_Password.getPassword());
    String rePass = new String(user_type_Re_enter_Password.getPassword());

    if (currentPass.isEmpty() || newPass.isEmpty() || rePass.isEmpty()) {
        JOptionPane.showMessageDialog(this, "All fields are required");
        return;
    }

    if (!newPass.equals(rePass)) {
        JOptionPane.showMessageDialog(this, "New passwords do not match");
        return;
    }

    JOptionPane.showMessageDialog(this, "Password changed successfully");
    this.dispose();
    }//GEN-LAST:event_ChangeActionPerformed


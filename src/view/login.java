    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      String user = jTextField1.getText().trim();
    String pass = new String(jPasswordField1.getPassword()).trim();

    if (user.isEmpty() || pass.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter both username and password.");
        return;
    }

    try {
        // --- ADD THIS SECTION FOR ADMIN ---
        int adminId = controller.authenticateAdmin(user, pass); 
        if (adminId != -1) {
            new view.AdminDashboard(adminId).setVisible(true); // Ensure you have this class
            this.dispose();
            return; 
        }
        // ----------------------------------

        // Try Company Login
        int companyId = controller.authenticateCompany(user, pass); 
        if (companyId != -1) {
            new view.CompanyDashboard(companyId).setVisible(true);
            this.dispose();
            return; 
        }

        // Try Seeker Login
        int seekerId = controller.authenticateSeeker(user, pass);
        if (seekerId != -1) {
            new view.UserDashboard(seekerId).setVisible(true); 
            this.dispose();
            return;
        }

        JOptionPane.showMessageDialog(this, "Invalid Username or Password", "Login Failed", JOptionPane.ERROR_MESSAGE);

    } catch (Exception e) {
        logger.log(java.util.logging.Level.SEVERE, "Database error", e);
        JOptionPane.showMessageDialog(this, "System Error: Unable to connect to database.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed



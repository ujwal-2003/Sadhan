   public CompanyHistory(int companyId) {
        this.companyId = companyId;
        initComponents();
        this.controller = new CompanyHistoryController(this);

        // 1. Setup Table Sorter
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        sorter = new TableRowSorter<>(model);
        jTable1.setRowSorter(sorter);

        // 2. Initial Data Load
        controller.loadTableData(companyId);
        txtDateSearch = new javax.swing.JTextField();
        
        // 3. Setup Search Field (Manual Listener Connection)
        setupSearchListener();

        // 4. Start Auto-Refresh Timer
        startHistoryAutoUpdate(30000);
    }

    /**
     * Connects the text field to the Controller.
     * This fixes the "unused" warning for the event method.
     */
   private void setupSearchListener() {
    jTextField1.addKeyListener(new java.awt.event.KeyAdapter() {
        @Override
        public void keyReleased(java.awt.event.KeyEvent evt) {
            // Get the text from the single search box
            String query = jTextField1.getText();
            
            // Pass it to the updated single-parameter controller method
            controller.applyFilter(query);
        }
    });
}


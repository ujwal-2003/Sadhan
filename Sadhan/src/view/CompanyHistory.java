    // --- Lifecycle and Timer Management ---

    private void startHistoryAutoUpdate(int interval) {
        historyTimer = new javax.swing.Timer(interval, e -> {
            // Only refresh if the panel is currently visible to the user
            if (this.isShowing()) {
                controller.loadTableData(this.companyId);
                System.out.println("History Auto-Refreshed for Company ID: " + companyId);
            }
        });
        historyTimer.start();
    }

    @Override
    public void removeNotify() {
        // Stops the timer when the panel is closed to prevent memory leaks
        if (historyTimer != null) {
            historyTimer.stop();
        }
        super.removeNotify();
    }


